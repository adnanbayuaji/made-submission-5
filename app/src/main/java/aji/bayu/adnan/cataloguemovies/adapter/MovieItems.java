public class MovieItems {
    private String mJudul;
    private String mDeskripsi;
    private String mTanggal;
    private String mGambar;
    private String mJumlahPeringkat;
    private String mPeringkat;

    public String getJudul() {
        return mJudul;
    }

    public void setJudul(String judul) {
        mJudul = judul;
    }

    public String getDeskripsi() {
        return mDeskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        mDeskripsi = deskripsi;
    }

    public String getTanggal() {
        return mTanggal;
    }

    public void setTanggal(String tanggal) {
        mTanggal = tanggal;
    }

    public String getJumlahPeringkat() {
        return mJumlahPeringkat;
    }

    public void setJumlahPeringkat(String jumlahPeringkat) {
        mJumlahPeringkat = jumlahPeringkat;
    }

    public String getPeringkat() {
        return mPeringkat;
    }

    public void setPeringkat(String peringkat) {
        mPeringkat = peringkat;
    }

    public String getGambar() {
        return mGambar;
    }

    public void setGambar(String gambar) {
        mGambar = gambar;
    }
}
